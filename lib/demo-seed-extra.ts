async function ensureWorkspace(userId: string, name: string, industry: string, processType: string) {
  const mine = await db.select({ ws: t.workspaces }).from(t.memberships)
    .innerJoin(t.workspaces, eq(t.workspaces.id, t.memberships.workspaceId))
    .where(eq(t.memberships.userId, userId));
  let ws = mine.map(r => r.ws).find(w => w.name === name && w.industry === industry);
  if (!ws) {
    [ws] = await db.insert(t.workspaces).values({ name, industry, processType }).returning();
    await db.insert(t.memberships).values({ userId, workspaceId: ws.id, role: "owner" });
  }
  return ws;
}

async function freshProject(workspaceId: string, baseName: string, description: string) {
  const clashes = await db.select({ name: t.projects.name }).from(t.projects)
    .where(and(eq(t.projects.workspaceId, workspaceId), like(t.projects.name, `${baseName}%`)));
  const name = clashes.length === 0 ? baseName : `${baseName} (${clashes.length + 1})`;
  const [project] = await db.insert(t.projects).values({ workspaceId, name, description }).returning();
  return project;
}

// ---- Semiconductor: etch CD drift on a plasma etcher -------------------------
export async function seedSemiconductorDemo(userId: string, deps: SeedDeps): Promise<{ projectId: string }> {
  const ws = await ensureWorkspace(userId, "Demo — Semiconductor", "electronics", "batch");
  const project = await freshProject(ws.id, "Demo: Etch CD Drift",
    "Seeded example — critical-dimension drift on a plasma etcher, with wafer-sort yield stages. Every alert, CAPA and decision was generated by the app's own rules, not inserted by hand.");

  // SPC stream (I-MR): one CD measurement per lot, backdated across ~3.5 weeks.
  const [spc] = await db.insert(t.streams).values({
    projectId: project.id, name: "Etch CD — Chamber B", type: "SPC_IMR",
    unit: "nm", subgroupSize: 1, specLow: 119.5, specHigh: 120.5, cpkThreshold: 1.67,
  }).returning();
  for (let i = 0; i < SEMI_SPC_VALUES.length; i++) {
    const [pt] = await db.insert(t.dataPoints).values({
      streamId: spc.id, ts: daysAgo(SEMI_SPC_VALUES.length - i),
      payload: { values: [SEMI_SPC_VALUES[i]] },
    }).returning();
    await runSpcRules(spc.id, pt.id); // WE flags + CPK_LOW fire here, for real
  }

  // Part D in action: two YIELD stages (litho, etch) — the etch stage runs
  // 3 consecutive days below its 92% threshold, so YIELD_BELOW_THRESHOLD
  // fires from the real runYieldRules; RTY multiplies across the stages.
  const stages: { name: string; threshold: number; entries: { started: number; passed: number; scrap: { reason: string; qty: number }[] }[] }[] = [
    {
      name: "Stage 1 — Litho", threshold: 0.95, entries: [
        { started: 400, passed: 391, scrap: [{ reason: "Overlay misalignment", qty: 6 }, { reason: "Resist defect", qty: 3 }] },
        { started: 380, passed: 372, scrap: [{ reason: "Overlay misalignment", qty: 5 }, { reason: "Particle", qty: 3 }] },
        { started: 410, passed: 402, scrap: [{ reason: "Resist defect", qty: 5 }, { reason: "Overlay misalignment", qty: 3 }] },
        { started: 395, passed: 388, scrap: [{ reason: "Overlay misalignment", qty: 4 }, { reason: "Particle", qty: 3 }] },
      ],
    },
    {
      name: "Stage 2 — Etch", threshold: 0.92, entries: [
        { started: 391, passed: 366, scrap: [{ reason: "CD out of spec", qty: 17 }, { reason: "Etch residue", qty: 8 }] },   // 93.6%
        { started: 372, passed: 341, scrap: [{ reason: "CD out of spec", qty: 23 }, { reason: "Etch residue", qty: 8 }] },   // 91.7% ↓
        { started: 402, passed: 365, scrap: [{ reason: "CD out of spec", qty: 29 }, { reason: "Micro-loading", qty: 8 }] },  // 90.8% ↓
        { started: 388, passed: 349, scrap: [{ reason: "CD out of spec", qty: 31 }, { reason: "Etch residue", qty: 8 }] },   // 89.9% ↓ → alert
      ],
    },
  ];
  for (const st of stages) {
    const [ys] = await db.insert(t.streams).values({
      projectId: project.id, name: st.name, type: "YIELD", subgroupSize: 1, target: st.threshold,
    }).returning();
    for (let i = 0; i < st.entries.length; i++) {
      const [pt] = await db.insert(t.dataPoints).values({
        streamId: ys.id, ts: daysAgo(st.entries.length - i, 17, 0), payload: st.entries[i],
      }).returning();
      await runYieldRules(ys.id, pt.id); // FPY computed + 3-consecutive rule, for real
    }
  }

  // Deviations: CD-OOS repeats 3× in the window → real recurrence rule drafts a CAPA
  // (electronics threshold = 3, per lib/terminology defaults).
  const DEFECTS = [
    { day: 16, code: "CD-OOS", area: "Etch Chamber B", qty: 12, severity: "major", description: "CD 119.6 nm at lot sort — low edge of spec" },
    { day: 12, code: "PARTICLE", area: "Litho track 1", qty: 3, severity: "minor", description: "Particle count spike after PM" },
    { day: 10, code: "CD-OOS", area: "Etch Chamber B", qty: 18, severity: "major", description: "CD trending low — chamber seasoning suspected" },
    { day: 6, code: "RESIST-DEFECT", area: "Litho track 1", qty: 4, severity: "minor", description: "Resist bubble on wafer edge" },
    { day: 3, code: "CD-OOS", area: "Etch Chamber B", qty: 24, severity: "critical", description: "CD 119.3 nm below LSL — lot held" },
  ];
  for (const d of DEFECTS) {
    const [nc] = await db.insert(t.nonConformances).values({
      projectId: project.id, date: daysAgo(d.day, 14, 0),
      defectCode: d.code, processArea: d.area, qty: d.qty, severity: d.severity, description: d.description,
    }).returning();
    await runNcRules(project.id, nc.id, "electronics");
  }

  // FMEA through the real action (server-side RPN + threshold alert at 80: strict default).
  const [fmea] = await db.insert(t.fmeas).values({
    projectId: project.id, name: "Etch process PFMEA", type: "PFMEA", rpnAction: 80,
  }).returning();
  const items = [
    { processStep: "OP30 — Plasma etch", failureMode: "CD below LSL", effect: "Device leakage; die fails wafer sort", cause: "Chamber seasoning shifts etch rate between cleans", severity: "8", occurrence: "5", detection: "3", recommendedAction: "Add CD feed-forward compensation from litho metrology; shorten clean interval", actionStatus: "open", linkedDefectCode: "CD-OOS" }, // RPN 120
    { processStep: "OP10 — Litho expose", failureMode: "Overlay misalignment", effect: "Short/open at metal layer", cause: "Stage calibration drift", severity: "7", occurrence: "3", detection: "3", recommendedAction: "Daily overlay verification wafer", actionStatus: "open", linkedDefectCode: "" }, // RPN 63
    { processStep: "OP30 — Plasma etch", failureMode: "Etch residue remains", effect: "Contact resistance high", cause: "Polymer buildup at low chamber temp", severity: "6", occurrence: "3", detection: "4", recommendedAction: "Temperature interlock before lot start", actionStatus: "none", linkedDefectCode: "" }, // RPN 72
  ];
  for (const it of items) {
    await deps.upsertFmeaItem(null, fd({ projectId: project.id, fmeaId: fmea.id, id: "", ...it }));
  }

  // Completed playbook run through the real advance action (decision-gate rows logged for real).
  const [run] = await db.insert(t.playbookRuns).values({ projectId: project.id, playbookKey: "reduce_defects" }).returning();
  const steps: Record<string, string>[] = [
    { process: "Plasma etch — Chamber B, all shifts", problem: "Wafer-sort CD-related yield loss doubled since the chamber wet clean in WW24: etch-stage FPY now ~90% vs 96% baseline, concentrated on Chamber B.", metric: "Etch-stage first-pass yield % (CD-related scrap)" },
    { baseline_days: "20" },
    { gate_choice: "Fix process capability (re-center or reduce variation)", gate_rationale: "The control chart shows a sustained downward CD drift (WE1–WE4 all fired) and rolling Cpk fell to 1.61 against the 1.67 threshold — this is the process drifting, not isolated events.", gate_decidedBy: "Etch module engineering" },
    { gate_choice: "Process change", gate_rationale: "Root cause is seasoning-driven etch-rate shift between cleans — implement CD feed-forward compensation and a shorter clean interval rather than chasing individual lots.", gate_decidedBy: "Etch module engineering" },
    { control_plan: "CD logged per lot on the I-MR chart; SPC alert holds the chamber pending etch-rate check; yield stages tracked daily with the 92% threshold; FMEA occurrence re-scored after 30 days." },
  ];
  for (const step of steps) await deps.advancePlaybook(null, fd({ projectId: project.id, runId: run.id, ...step }));

  // Resolve the earliest flagged SPC entries through the real action (old news the team handled).
  const spcAlerts = await db.select().from(t.alerts).where(and(
    eq(t.alerts.projectId, project.id), eq(t.alerts.sourceType, "SPC"))).orderBy(t.alerts.createdAt);
  const firstEntry = spcAlerts[0]?.sourceId;
  for (const a of spcAlerts.filter(a => a.sourceId === firstEntry)) {
    await deps.setAlertStatus(null, fd({ projectId: project.id, id: a.id, status: "resolved" }));
  }
  return { projectId: project.id };
}

