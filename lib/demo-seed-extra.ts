// Part O — Multi-industry demo seeds: Semiconductor and Warehouse & Logistics.
// Same design rule as lib/demo-seed.ts (which is untouched): everything goes
// through the SAME code paths a real user exercises — every SPC/OEE/YIELD
// point runs its real rules function on entry, every defect runs runNcRules
// (recurrence → auto-drafted CAPA), FMEA items go through the real
// upsertFmeaItem action, the playbook is advanced step-by-step through the
// real guided/advance actions, and nothing is written into `computed`,
// `alerts`, `decisions`, or `capas` by hand. The SPC series below are
// verified in scripts/verify-calcs.ts through the real lib/spc.ts functions:
// they stay in control for the stable prefix, then trigger the documented
// Western Electric rules, and (semiconductor) drop rolling Cpk below the
// strict 1.67 industry threshold on the final points.

import { db, t } from "@/db";
import { and, eq, like } from "drizzle-orm";
import { runSpcRules, runNcRules, runOeeRules } from "@/lib/rules";
import { runYieldRules } from "@/lib/yield";
import type { SeedDeps } from "@/lib/demo-seed";

const fd = (obj: Record<string, string>) => {
  const f = new FormData();
  for (const [k, v] of Object.entries(obj)) f.set(k, v);
  return f;
};
const daysAgo = (n: number, hour = 10, minute = 30) => {
  const d = new Date();
  d.setDate(d.getDate() - n);
  d.setHours(hour, minute, 0, 0);
  return d;
};

// Etch CD (nm), nominal 120.0, LSL 119.5 / USL 120.5, Cpk threshold 1.67
// (strict electronics/semi default). First 16 points stable; then a chamber-
